package model;

public class MenuTable {
	 
	 private int id;
	 private String hoge;
	 /**
	  * @return id
	  */
	 public int getId() {
	  return id;
	 }
	 /**
	  * @param id セットする id
	  */
	 public void setId(int id) {
	  this.id = id;
	 }
	 /**
	  * @return hoge
	  */
	 public String getHoge() {
	  return hoge;
	 }
	 /**
	  * @param hoge セットする hoge
	  */
	 public void setHoge(String hoge) {
	  this.hoge = hoge;
	 }
	}
